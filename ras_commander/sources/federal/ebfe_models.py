    @staticmethod
    def _standardize_hdf_asset_references(project_folder: Path) -> Dict[str, int]:
        """Update HDF terrain/land-cover filename attributes to local folders."""
        try:
            import h5py
            import numpy as np
        except Exception:
            return {'hdf_asset_references_updated': 0}

        asset_lookup = {}
        for folder_name in ["Terrain", "Land Cover"]:
            folder = project_folder / folder_name
            if not folder.exists():
                continue
            for asset in folder.rglob("*"):
                if asset.is_file():
                    key = asset.name.lower()
                    existing = asset_lookup.get(key)
                    if existing is None:
                        asset_lookup[key] = asset
                        continue
                    try:
                        asset_depth = len(asset.relative_to(folder).parts)
                        existing_depth = len(existing.relative_to(folder).parts)
                    except ValueError:
                        asset_depth = existing_depth = 999
                    if asset_depth < existing_depth:
                        asset_lookup[key] = asset

        if not asset_lookup:
            return {'hdf_asset_references_updated': 0}

        terrain_default = asset_lookup.get("terrain.hdf")
        land_cover_default = next(
            (
                asset for asset in asset_lookup.values()
                if asset.name.lower() == "mannings_n.tif"
                and asset.suffix.lower() == ".tif"
            ),
            None,
        ) or next(
            (
                asset for asset in asset_lookup.values()
                if "land cover" in str(asset.parent).lower()
                and asset.suffix.lower() == ".tif"
            ),
            None,
        ) or next(
            (
                asset for asset in asset_lookup.values()
                if "land cover" in str(asset.parent).lower()
                and asset.suffix.lower() == ".hdf"
            ),
            None,
        )

        updated = 0
        hdf_files = list(project_folder.glob("*.g[0-9][0-9].hdf"))
        hdf_files.extend(project_folder.glob("*.p[0-9][0-9].hdf"))

        for hdf_file in hdf_files:
            try:
                with h5py.File(hdf_file, "r+") as hdf:
                    def update_attrs(_name, obj):
                        nonlocal updated
                        for attr_name in list(obj.attrs.keys()):
                            attr_lower = str(attr_name).lower()
                            is_terrain = "terrain filename" in attr_lower
                            is_land_cover = (
                                "land cover filename" in attr_lower
                                or "landcover filename" in attr_lower
                                or "infiltration filename" in attr_lower
                                or "soils filename" in attr_lower
                                or "soil filename" in attr_lower
                            )
                            if not is_terrain and not is_land_cover:
                                continue
                            raw_value = obj.attrs[attr_name]
                            if isinstance(raw_value, (bytes, bytearray)):
                                current_value = raw_value.decode(
                                    "utf-8",
                                    errors="ignore",
                                )
                            elif hasattr(raw_value, "decode"):
                                current_value = raw_value.decode(
                                    "utf-8",
                                    errors="ignore",
                                )
                            else:
                                current_value = str(raw_value)
                            asset_name = Path(current_value).name.lower()
                            destination = asset_lookup.get(asset_name)
                            if destination is None and is_terrain:
                                destination = terrain_default
                            if destination is None and is_land_cover:
                                destination = land_cover_default
                            if destination is None:
                                continue
                            new_value = RasEbfeModels._format_windows_relative(
                                destination,
                                project_folder,
                            )
                            if current_value == new_value:
                                if not hasattr(raw_value, "decode"):
                                    obj.attrs[attr_name] = np.bytes_(new_value)
                                    updated += 1
                                continue
                            obj.attrs[attr_name] = np.bytes_(new_value)
                            updated += 1

                    update_attrs("/", hdf)
                    hdf.visititems(update_attrs)
            except Exception:
                continue

        return {'hdf_asset_references_updated': updated}

    @staticmethod
    def _normalize_crs_for_compare(crs_text: Optional[str]) -> Optional[Any]:
        """Return a pyproj CRS for robust CRS equality checks."""
        if not crs_text:
            return None
        try:
            from pyproj import CRS

            return CRS.from_user_input(crs_text)
        except Exception:
            return None

    @staticmethod
    def _crs_matches(left: Optional[str], right: Optional[str]) -> bool:
        """Compare two CRS definitions, treating missing CRS as inconclusive."""
        left_crs = RasEbfeModels._normalize_crs_for_compare(left)
        right_crs = RasEbfeModels._normalize_crs_for_compare(right)
        if left_crs is None or right_crs is None:
            return True
        try:
            return bool(left_crs.equals(right_crs, ignore_axis_order=True))
        except TypeError:
            return bool(left_crs == right_crs)

    @staticmethod
    def _read_asset_projection(asset_path: Path) -> Optional[str]:
        """Read projection text from a terrain HDF, raster, or PRJ file."""
        try:
            from ras_commander.hdf import HdfBase
        except Exception:
            return None

        asset_path = Path(asset_path)
        try:
            if asset_path.suffix.lower() in {".hdf", ".h5"}:
                return HdfBase._get_projection_from_hdf_attribute(asset_path)
            if asset_path.suffix.lower() in {".tif", ".tiff", ".vrt"}:
                return HdfBase._get_projection_from_raster(asset_path)
            if asset_path.suffix.lower() == ".prj":
                return HdfBase._get_projection_from_prj_file(asset_path)
        except Exception:
            return None
        return None

    @staticmethod
    def _rasmap_projection_file(project_folder: Path) -> Optional[Path]:
        """Return the local projection file referenced by a project rasmap."""
        project_folder = Path(project_folder)
        for rasmap_file in sorted(project_folder.glob("*.rasmap")):
            content = rasmap_file.read_text(encoding='utf-8', errors='ignore')
            match = re.search(
                r'RASProjectionFilename[^>]*Filename="([^"]+)"',
                content,
                flags=re.IGNORECASE,
            )
            if match:
                projection = RasEbfeModels._resolve_model_reference(
                    project_folder,
                    match.group(1),
                )
                if projection.exists():
                    return projection

            text_match = re.search(
                r'<RASProjectionFilename>\s*([^<]+?)\s*</RASProjectionFilename>',
                content,
                flags=re.IGNORECASE,
            )
            if text_match:
                projection = RasEbfeModels._resolve_model_reference(
                    project_folder,
                    text_match.group(1),
                )
                if projection.exists():
                    return projection
        return None

    @staticmethod
    def _rasmap_terrain_hdf(project_folder: Path) -> Optional[Path]:
        """Return the terrain HDF referenced by a project rasmap."""
        project_folder = Path(project_folder)
        for rasmap_file in sorted(project_folder.glob("*.rasmap")):
            content = rasmap_file.read_text(encoding='utf-8', errors='ignore')
            match = re.search(
                r'<Layer[^>]*Type="TerrainLayer"[^>]*Filename="([^"]+)"',
                content,
                flags=re.IGNORECASE,
            )
            if match:
                terrain = RasEbfeModels._resolve_model_reference(
                    project_folder,
                    match.group(1),
                )
                if terrain.exists():
                    return terrain
        terrains = sorted(project_folder.glob("Terrain/**/*.hdf"))
        return terrains[0] if terrains else None

    @staticmethod
    def _update_project_terrain_reference(
        project_folder: Path,
        terrain_hdf: Path,
    ) -> int:
        """Point rasmap and HDF terrain references at ``terrain_hdf``."""
        updates = 0
        project_folder = Path(project_folder)
        terrain_hdf = Path(terrain_hdf)
        desired = RasEbfeModels._format_windows_relative(terrain_hdf, project_folder)
        for rasmap_file in sorted(project_folder.glob("*.rasmap")):
            content = rasmap_file.read_text(encoding='utf-8', errors='ignore')
            pattern = re.compile(
                r'(<Layer[^>]*Type="TerrainLayer"[^>]*Filename=")([^"]+)("[^>]*>)',
                flags=re.IGNORECASE,
            )
            updated = pattern.sub(
                lambda match: f"{match.group(1)}{desired}{match.group(3)}",
                content,
            )
            updated = re.sub(
                r'(<TerrainDestinationFolder>)([^<]*)(</TerrainDestinationFolder>)',
                r'\1.\\Terrain\3',
                updated,
                flags=re.IGNORECASE,
            )
            if updated != content:
                rasmap_file.write_text(updated, encoding='utf-8')
                updates += 1

        try:
            import h5py
            import numpy as np
        except Exception:
            return updates

        hdf_files = list(project_folder.glob("*.g[0-9][0-9].hdf"))
        hdf_files.extend(project_folder.glob("*.p[0-9][0-9].hdf"))
        for hdf_file in hdf_files:
            try:
                with h5py.File(hdf_file, "r+") as hdf:
                    changed = False

                    def update_attrs(_name, obj):
                        nonlocal changed
                        for attr_name in list(obj.attrs.keys()):
                            if "terrain filename" not in str(attr_name).lower():
                                continue
                            raw_value = obj.attrs[attr_name]
                            if isinstance(raw_value, (bytes, bytearray)):
                                current = raw_value.decode("utf-8", errors="ignore")
                            elif hasattr(raw_value, "decode"):
                                current = raw_value.decode("utf-8", errors="ignore")
                            else:
                                current = str(raw_value)
                            if current == desired:
                                continue
                            obj.attrs[attr_name] = np.bytes_(desired)
                            changed = True

                    update_attrs("/", hdf)
                    hdf.visititems(update_attrs)
                    if changed:
                        updates += 1
            except Exception:
                continue
        return updates

    @staticmethod
    def _find_terrain_source_rasters(source_folder: Optional[Path]) -> List[Path]:
        """Find source rasters suitable for rebuilding terrain."""
        if source_folder is None:
            return []
        source_folder = Path(source_folder)
        if not source_folder.exists():
            return []
        raster_suffixes = {".tif", ".tiff", ".vrt", ".flt", ".img"}
        rasters = [
            path for path in source_folder.rglob("*")
            if path.is_file() and path.suffix.lower() in raster_suffixes
        ]
        return sorted(rasters)

    @staticmethod
    def _reproject_raster_for_terrain(
        source_raster: Path,
        target_prj: Path,
        destination: Path,
        gdal_version: str,
        timeout: int,
    ) -> Path:
        """Use HEC-RAS bundled GDAL to reproject a raster to ``target_prj``."""
        if destination.exists():
            return destination

        from ras_commander.terrain import RasTerrain

        gdal_path = RasTerrain._get_hecras_gdal_path(gdal_version)
        gdalwarp = gdal_path / "gdalwarp.exe"
        if not gdalwarp.exists():
            raise FileNotFoundError(f"gdalwarp.exe not found: {gdalwarp}")

        destination.parent.mkdir(parents=True, exist_ok=True)
        target_wkt = Path(target_prj).read_text(encoding='utf-8', errors='ignore')
        cmd = [
            str(gdalwarp),
            "-overwrite",
            "-t_srs",
            target_wkt,
            "-r",
            "bilinear",
            "-of",
            "GTiff",
            "-co",
            "TILED=YES",
            "-co",
            "COMPRESS=LZW",
            "-co",
            "BIGTIFF=IF_SAFER",
            str(source_raster),
            str(destination),
        ]
        result = subprocess.run(
            cmd,
            capture_output=True,
            text=True,
            timeout=timeout,
        )
        if result.returncode != 0:
            raise RuntimeError(
                "gdalwarp failed while rebuilding eBFE terrain. "
                f"Return code: {result.returncode}. STDERR: {result.stderr}"
            )
        if not destination.exists():
            raise RuntimeError(f"Reprojected raster was not created: {destination}")
        return destination

    @staticmethod
    def _rebuild_mismatched_project_terrains(
        ras_model_folder: Path,
        source_terrain_folder: Optional[Path],
        terrain_create_ras_version: str = "6.6",
        gdal_version: str = "6.6",
        timeout: int = 7200,
    ) -> List[Dict[str, Any]]:
        """Reproject/rebuild project terrains whose CRS differs from rasmap CRS."""
        from ras_commander.terrain import RasTerrain

        records: List[Dict[str, Any]] = []
        source_rasters = RasEbfeModels._find_terrain_source_rasters(
            source_terrain_folder
        )

        for project_folder in RasEbfeModels._discover_valid_ras_projects(
            ras_model_folder
        ):
            projection_file = RasEbfeModels._rasmap_projection_file(project_folder)
            terrain_hdf = RasEbfeModels._rasmap_terrain_hdf(project_folder)
            if projection_file is None or terrain_hdf is None:
                continue

            project_crs = RasEbfeModels._read_asset_projection(projection_file)
            terrain_crs = RasEbfeModels._read_asset_projection(terrain_hdf)
            rebuild_reasons = []
            if not RasEbfeModels._crs_matches(project_crs, terrain_crs):
                rebuild_reasons.append(
                    "terrain HDF CRS does not match the rasmap projection"
                )

            mismatched_rasters = []
            for raster in source_rasters:
                raster_crs = RasEbfeModels._read_asset_projection(raster)
                if not RasEbfeModels._crs_matches(project_crs, raster_crs):
                    mismatched_rasters.append(str(raster))
            if mismatched_rasters:
                rebuild_reasons.append(
                    "source terrain raster CRS does not match the rasmap "
                    "projection"
                )

            if not rebuild_reasons:
                continue

            record: Dict[str, Any] = {
                "project_folder": str(project_folder),
                "status": "pending",
                "reason": "; ".join(rebuild_reasons),
                "projection_file": str(projection_file),
                "original_terrain": str(terrain_hdf),
                "mismatched_source_rasters": mismatched_rasters,
            }

            if not source_rasters:
                record["status"] = "blocked_no_source_rasters"
                records.append(record)
                continue

            try:
                rebuild_root = project_folder / "Terrain" / "Rebuilt"
                reprojected_rasters = []
                for raster in source_rasters:
                    destination = (
                        rebuild_root
                        / "reprojected"
                        / f"{raster.stem}_{projection_file.stem}.tif"
                    )
                    reprojected_rasters.append(
                        RasEbfeModels._reproject_raster_for_terrain(
                            raster,
                            projection_file,
                            destination,
                            gdal_version,
                            timeout,
                        )
                    )

                rebuilt_hdf = (
                    project_folder
                    / "Terrain"
                    / f"Terrain_{projection_file.stem}.hdf"
                )
                RasTerrain.create_terrain_hdf(
                    input_rasters=reprojected_rasters,
                    output_hdf=rebuilt_hdf,
                    projection_prj=projection_file,
                    units="Feet",
                    stitch=True,
                    hecras_version=terrain_create_ras_version,
                    timeout=timeout,
                )
                rasmap_updates = RasEbfeModels._update_project_terrain_reference(
                    project_folder,
                    rebuilt_hdf,
                )
                record.update({
                    "status": "rebuilt",
                    "rebuilt_terrain": str(rebuilt_hdf),
                    "source_raster_count": len(source_rasters),
                    "rasmap_updates": rasmap_updates,
                })
            except Exception as exc:
                record.update({
                    "status": "failed",
                    "error": str(exc),
                    "source_raster_count": len(source_rasters),
                })

            records.append(record)

        return records

