    @staticmethod
    def _extract_component(
        zip_path: Path,
        dest: Path,
        description: str = ""
    ):
        """Extract a component zip, skip if already extracted."""
        if dest.exists() and any(dest.iterdir()):
            print(f"  Already extracted: {dest.name}/")
            return
        dest.mkdir(parents=True, exist_ok=True)
        print(f"  Extracting {description or zip_path.name}...")
        with zipfile.ZipFile(zip_path, 'r') as zf:
            total = sum(info.file_size for info in zf.filelist)
            with tqdm(total=total, unit='B', unit_scale=True, desc=f"    {zip_path.name}") as pbar:
                for member in zf.filelist:
                    zf.extract(member, dest)
                    pbar.update(member.file_size)

    @staticmethod
    def _download_and_extract(
        url: str,
        output_folder: Path,
        description: str = "file"
    ) -> Path:
        """
        Download and extract a zip file from S3.

        Args:
            url: S3 download URL
            output_folder: Where to extract the contents
            description: Description for progress bar

        Returns:
            Path to extracted folder
        """
        output_folder = Path(output_folder)
        output_folder.mkdir(parents=True, exist_ok=True)

        # Determine zip filename from URL
        zip_filename = url.split('/')[-1]
        zip_path = output_folder / zip_filename
        partial_zip_path = RasEbfeModels._get_partial_download_path(zip_path)

        # Download if not already present
        download_needed = False

        if zip_path.exists():
            # Validate existing zip file
            try:
                with zipfile.ZipFile(zip_path, 'r') as zf:
                    # Quick test - just open, don't extract
                    pass
                if partial_zip_path.exists():
                    partial_zip_path.unlink()
                print(f"\n✓ {description} already downloaded: {zip_path}")
            except zipfile.BadZipFile:
                print(f"\n⚠️ Existing file is corrupted, re-downloading...")
                zip_path.unlink()  # Delete corrupted file
                if partial_zip_path.exists():
                    partial_zip_path.unlink()
                download_needed = True
        else:
            download_needed = True

        if download_needed:
            print(f"\nDownloading {description}...")
            print(f"  URL: {url}")
            print(f"  Destination: {zip_path}")

            RasEbfeModels._download_file(
                url=url,
                dest=zip_path,
                description=description
            )

            total_size = zip_path.stat().st_size
            print(f"  ✓ Downloaded {total_size / 1e9:.1f} GB")

        # Extract if not already extracted
        extracted_folder_name = zip_filename.replace('.zip', '_extracted')
        extracted_folder = output_folder / extracted_folder_name

        if not extracted_folder.exists():
            print(f"\nExtracting {description}...")
            with zipfile.ZipFile(zip_path, 'r') as zip_ref:
                # Get total uncompressed size for progress bar
                total_size = sum(info.file_size for info in zip_ref.filelist)

                with tqdm(total=total_size, unit='B', unit_scale=True, desc=f"  Extracting") as pbar:
                    for member in zip_ref.filelist:
                        zip_ref.extract(member, extracted_folder)
                        pbar.update(member.file_size)

            print(f"  ✓ Extracted to: {extracted_folder}")
        else:
            print(f"✓ {description} already extracted: {extracted_folder}")

        return extracted_folder

    @staticmethod
    def _select_archive_source_root(downloaded_folder: Path) -> Path:
        """Pick the most useful source root inside a single extracted archive."""
        downloaded_folder = Path(downloaded_folder)
        projects = RasEbfeModels._discover_valid_ras_projects(downloaded_folder)
        if not projects:
            return downloaded_folder

        try:
            common_path = Path(os.path.commonpath([str(path) for path in projects]))
        except ValueError:
            common_path = downloaded_folder

        # If all projects sit inside one top-level archive folder, copy that
        # folder's contents rather than preserving the synthetic extraction name.
        try:
            relative_parts = common_path.relative_to(downloaded_folder).parts
        except ValueError:
            return downloaded_folder

        if not relative_parts:
            return downloaded_folder

        candidate = downloaded_folder / relative_parts[0]
        if candidate.exists() and candidate.is_dir():
            return candidate
        return downloaded_folder

    @staticmethod
    def _select_split_delivery_source_root(component_root: Path) -> Path:
        """Find the parent folder for an extracted split eBFE component.

        Pattern 2 archives commonly contain sibling ``Input/``, ``Terrain/``,
        ``LandCover/``, and ``Output/`` folders. The best source root is the
        folder that contains those siblings, not the nested project folder.
        """
        component_root = Path(component_root)
        if not component_root.exists():
            return component_root

        candidate_roots = {component_root}
        split_names = {
            "input",
            "terrain",
            "output",
            "outputs",
            "landcover",
            "land cover",
            "landuse",
            "dss",
            "dss input",
            "dss inputs",
        }
        for path in component_root.rglob("*"):
            if path.is_dir() and path.name.lower() in split_names:
                candidate_roots.add(path.parent)

        def score(candidate: Path) -> tuple[int, int]:
            names = {
                child.name.lower()
                for child in candidate.iterdir()
                if child.is_dir()
            }
            split_score = sum(1 for name in names if name in split_names)
            input_root = next(
                (
                    candidate / name
                    for name in ("Input", "input")
                    if (candidate / name).exists()
                ),
                candidate,
            )
            try:
                project_count = len(
                    RasEbfeModels._discover_valid_ras_projects(input_root)
                )
            except Exception:
                project_count = 0
            try:
                depth = len(candidate.relative_to(component_root).parts)
            except ValueError:
                depth = 999
            return (split_score * 100 + project_count * 10 - depth, -depth)

        best = max(candidate_roots, key=score)
        if score(best)[0] <= 0:
            return RasEbfeModels._select_archive_source_root(component_root)
        return best

