    @staticmethod
    def _normalize_north_galveston_ras_submission(ras_folder: Path) -> Dict[str, int]:
        """
        Flatten North Galveston Bay RAS_Submittal components into project folders.

        The source zip contains Input.zip, Terrain.zip, LandCover.zip, and
        Output.zip. After extraction, this method copies Input project files into
        openable project folders and copies Terrain/Land Cover/Output assets into
        each project folder so validation can run in place.
        """
        ras_folder = Path(ras_folder)
        submittal = ras_folder / "RAS_Submittal"
        component_root = submittal if submittal.exists() else ras_folder

        input_root = next(
            (
                candidate for candidate in [
                    component_root / "Input",
                    ras_folder / "Input",
                ]
                if candidate.exists()
            ),
            None,
        )
        if input_root is None:
            return {'projects_normalized': 0, 'files_copied': 0}

        terrain_root = next(
            (
                candidate for candidate in [
                    component_root / "Terrain",
                    ras_folder / "Terrain",
                ]
                if candidate.exists()
            ),
            None,
        )
        land_cover_root = next(
            (
                candidate for candidate in [
                    component_root / "LandCover",
                    component_root / "Land Cover",
                    ras_folder / "LandCover",
                    ras_folder / "Land Cover",
                ]
                if candidate.exists()
            ),
            None,
        )
        output_root = next(
            (
                candidate for candidate in [
                    component_root / "Output",
                    ras_folder / "Output",
                ]
                if candidate.exists()
            ),
            None,
        )

        input_projects = RasEbfeModels._discover_valid_ras_projects(input_root)
        if not input_projects and any(input_root.glob("*.prj")):
            input_projects = [input_root]

        files_copied = 0
        for project in input_projects:
            if len(input_projects) == 1:
                destination = ras_folder
            else:
                destination = ras_folder / project.name

            files_copied += RasEbfeModels._move_tree_contents(
                project,
                destination,
                skip_suffixes={".zip"},
            )

            if terrain_root is not None:
                files_copied += RasEbfeModels._move_tree_contents(
                    terrain_root,
                    destination / "Terrain",
                    skip_suffixes={".zip"},
                )

            if land_cover_root is not None:
                files_copied += RasEbfeModels._move_tree_contents(
                    land_cover_root,
                    destination / "Land Cover",
                    skip_suffixes={".zip"},
                )

            if output_root is not None:
                files_copied += RasEbfeModels._move_tree_contents(
                    output_root,
                    destination,
                    skip_suffixes={".zip"},
                )

        return {
            'projects_normalized': len(input_projects),
            'files_copied': files_copied,
        }

    @staticmethod
    def _validate_dss_files(ras_model_folder: Path) -> List[Dict]:
        """Validate DSS files using ras-commander."""
        try:
            from ras_commander.dss import RasDss
        except ImportError:
            print("  ⚠️ ras-commander.dss not available - skipping validation")
            return []

        dss_files = list(ras_model_folder.glob('**/*.dss'))
        if not dss_files:
            print("  No DSS files found")
            return []

        print(f"  Found {len(dss_files)} DSS file(s)")
        validation_results = []

        for dss_file in dss_files:
            print(f"\n  Validating: {dss_file.name}")
            try:
                catalog = RasDss.get_catalog(dss_file)
                pathnames = catalog["pathname"].astype(str).tolist()
                pathname_count = len(pathnames)
                print(f"    Pathnames: {pathname_count}")

                # NOTE:
                # Many DSS files contain mixed record types (time series, paired data,
                # grids, etc.). A "read_timeseries" check on arbitrary catalog entries
                # can produce false negatives even when the DSS file is perfectly fine
                # for HEC-RAS (which only needs specific referenced records).
                #
                # So our "organize" validation is intentionally lightweight:
                # - File is readable (catalog can be fetched)
                # - Pathname formats look structurally correct
                format_errors = 0
                format_warnings = 0

                try:
                    from ras_commander.RasValidation import ValidationSeverity
                except Exception:
                    ValidationSeverity = None

                for pn in pathnames:
                    r = RasDss.check_pathname_format(pn)
                    passed = (
                        r.get("passed", False)
                        if isinstance(r, dict)
                        else getattr(r, "passed", False)
                    )
                    if not passed:
                        format_errors += 1
                        continue
                    if ValidationSeverity is not None:
                        severity = getattr(r, "severity", None)
                        if severity == ValidationSeverity.WARNING:
                            format_warnings += 1

                ok = format_errors == 0
                validation_results.append({
                    "file": dss_file.name,
                    "total_pathnames": pathname_count,
                    "format_errors": format_errors,
                    "format_warnings": format_warnings,
                    "valid": ok,
                })

                if ok:
                    print(f"    ✓ Format OK ({format_warnings} warnings)")
                else:
                    print(f"    ⚠️ Format errors: {format_errors} ({format_warnings} warnings)")

            except Exception as e:
                print(f"    ✗ Error: {e}")
                validation_results.append({
                    'file': dss_file.name,
                    'total_pathnames': 0,
                    'valid': False,
                    'error': str(e)
                })

        return validation_results

