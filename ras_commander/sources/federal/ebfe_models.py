    @staticmethod
    def _standardize_rasmap_assets(project_folder: Path) -> Dict[str, int]:
        """Standardize terrain and land cover folders referenced by .rasmap."""
        project_folder = Path(project_folder)
        terrain_updates = 0
        land_cover_updates = 0

        for rasmap_file in sorted(project_folder.glob("*.rasmap")):
            content = rasmap_file.read_text(encoding='utf-8', errors='ignore')
            original = content

            def replace_layer_asset(
                layer_match: re.Match,
                folder_name: str,
            ) -> str:
                nonlocal terrain_updates, land_cover_updates
                prefix = layer_match.group(1)
                old_value = layer_match.group(2)
                suffix = layer_match.group(3)
                source = RasEbfeModels._resolve_model_reference(
                    project_folder,
                    old_value,
                )
                if not source.exists():
                    found = RasEbfeModels._find_asset_by_name(
                        project_folder,
                        old_value,
                    )
                    if found is None:
                        return layer_match.group(0)
                    source = found

                destination = RasEbfeModels._copy_asset_with_sidecars(
                    source,
                    project_folder / folder_name,
                )
                new_value = RasEbfeModels._format_windows_relative(
                    destination,
                    project_folder,
                )
                if new_value != old_value:
                    if folder_name == "Terrain":
                        terrain_updates += 1
                    else:
                        land_cover_updates += 1
                return f"{prefix}{new_value}{suffix}"

            terrain_pattern = re.compile(
                r'(<Layer[^>]*Type="TerrainLayer"[^>]*Filename=")([^"]+)("[^>]*>)',
                re.IGNORECASE,
            )
            content = terrain_pattern.sub(
                lambda match: replace_layer_asset(match, "Terrain"),
                content,
            )

            land_cover_pattern = re.compile(
                r'(<Layer[^>]*Type="[^"]*LandCover[^"]*"[^>]*Filename=")([^"]+)("[^>]*>)',
                re.IGNORECASE,
            )
            content = land_cover_pattern.sub(
                lambda match: replace_layer_asset(match, "Land Cover"),
                content,
            )

            legacy_land_cover_pattern = re.compile(
                r'(<Layer[^>]*Filename=")([^"]*LandCover[^"]+)("[^>]*>)',
                re.IGNORECASE,
            )
            content = legacy_land_cover_pattern.sub(
                lambda match: replace_layer_asset(match, "Land Cover"),
                content,
            )

            content = re.sub(
                r'(<TerrainDestinationFolder>)([^<]*)(</TerrainDestinationFolder>)',
                r'\1.\\Terrain\3',
                content,
                flags=re.IGNORECASE,
            )
            content = re.sub(
                r'(<LandCoverDestinationFolder>)([^<]*)(</LandCoverDestinationFolder>)',
                r'\1.\\Land Cover\3',
                content,
                flags=re.IGNORECASE,
            )
            content = re.sub(
                r'(<AddDataFolder>)([^<]*LandCover[^<]*)(</AddDataFolder>)',
                r'\1.\\Land Cover\3',
                content,
                flags=re.IGNORECASE,
            )

            source_folder_pattern = re.compile(
                r'(<TerrainSourceFolder>)([^<]*)(</TerrainSourceFolder>)',
                re.IGNORECASE,
            )
            source_match = source_folder_pattern.search(content)
            if source_match:
                current_source = source_match.group(2).strip()
                if current_source and not current_source.lower().startswith(".\\terrain"):
                    content = source_folder_pattern.sub(
                        r'\1.\\Terrain\3',
                        content,
                        count=1,
                    )

            if content != original:
                rasmap_file.write_text(content, encoding='utf-8')

        return {
            'terrain_references_updated': terrain_updates,
            'land_cover_references_updated': land_cover_updates,
        }

    @staticmethod
    def _standardize_precomputed_outputs(project_folder: Path) -> Dict[str, int]:
        """Move precomputed HEC-RAS outputs from Output/ into project root."""
        project_folder = Path(project_folder)
        output_folders = [
            folder for folder in [
                project_folder / "Output",
                project_folder / "Outputs",
                project_folder / "Results",
            ]
            if folder.exists() and folder.is_dir()
        ]
        moved = 0
        output_patterns = [
            re.compile(r".+\.p\d\d\.hdf$", re.IGNORECASE),
            re.compile(r".+\.p\d\d\.blf$", re.IGNORECASE),
            re.compile(r".+\.bco\d\d$", re.IGNORECASE),
            re.compile(r".+\.comp_msgs\.txt$", re.IGNORECASE),
            re.compile(r".+\.computeMsgs\.txt$", re.IGNORECASE),
        ]

        for output_folder in output_folders:
            for source in sorted(output_folder.rglob("*")):
                if not source.is_file():
                    continue
                if not any(pattern.match(source.name) for pattern in output_patterns):
                    continue
                destination = project_folder / source.name
                if destination.exists():
                    continue
                shutil.move(str(source), str(destination))
                moved += 1

        return {'output_assets_moved': moved}

    @staticmethod
    def _standardize_plan_references(project_folder: Path) -> Dict[str, int]:
        """Remove project plan references whose .p## files are absent."""
        project_folder = Path(project_folder)
        removed = 0
        for prj_file in project_folder.glob("*.prj"):
            if not RasEbfeModels._is_hecras_project_prj(prj_file):
                continue
            lines = prj_file.read_text(
                encoding='utf-8',
                errors='ignore',
            ).splitlines()
            updated_lines = []
            for line in lines:
                match = re.match(r"^(Plan File=)(p\d\d)\s*$", line, re.IGNORECASE)
                if not match:
                    updated_lines.append(line)
                    continue
                plan_file = project_folder / f"{prj_file.stem}.{match.group(2)}"
                if plan_file.exists():
                    updated_lines.append(line)
                    continue
                removed += 1
            if updated_lines != lines:
                prj_file.write_text(
                    "\n".join(updated_lines) + "\n",
                    encoding='utf-8',
                )
        return {'missing_plan_references_removed': removed}

